package tasksAIU;

public class Oops2 {
    public static void main(String[] args) {
        int x = 0;
        System.out.printf("x is %d\n", x);

        x += 15.2;   // set x to 15.2
        System.out.printf("x is now %d\n", x);

        int y = x + 1;   // set y to 1 more than x
        System.out.println("x and y are " + x +" and "+ y);
    }
}
